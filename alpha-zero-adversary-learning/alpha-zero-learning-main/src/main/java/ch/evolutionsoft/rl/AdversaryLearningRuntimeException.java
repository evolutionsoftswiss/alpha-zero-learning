package ch.evolutionsoft.rl;

public class AdversaryLearningRuntimeException extends RuntimeException {

  public AdversaryLearningRuntimeException() {
    super();
  }

  public AdversaryLearningRuntimeException(String message) {
    super(message);
  }

  public AdversaryLearningRuntimeException(Throwable cause) {
    super(cause);
  }

  public AdversaryLearningRuntimeException(String message, Throwable cause) {
    super(message, cause);
  }

}
