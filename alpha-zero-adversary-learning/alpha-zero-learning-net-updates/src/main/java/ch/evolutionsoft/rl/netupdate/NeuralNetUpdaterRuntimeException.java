package ch.evolutionsoft.rl.netupdate;

public class NeuralNetUpdaterRuntimeException extends RuntimeException {

  public NeuralNetUpdaterRuntimeException() {
    super();
  }

  public NeuralNetUpdaterRuntimeException(String message) {
    super(message);
  }

  public NeuralNetUpdaterRuntimeException(Throwable cause) {
    super(cause);
  }

  public NeuralNetUpdaterRuntimeException(String message, Throwable cause) {
    super(message, cause);
  }

}
